#include <stdio.h>

int main(){

    /* 7. Mostrar os números pares e ímpares de 0 a 200 utilizando o for. */

printf("xX Numeros Pares Xx");
for(int i = 0; i <= 200; i+=2){


    printf("\n%d", i);
}

printf("xX Numeros impares Xx");
for(int i2 = 1; i2 <= 200; i2+=2){


    printf("\n%d", i2);

}

return 0;
}
